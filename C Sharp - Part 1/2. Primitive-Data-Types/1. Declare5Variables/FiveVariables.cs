using System;


class FiveVariables
{
    static void Main()
    {
        ushort first = 52130;
        sbyte second = -115;
        int third = 4825932;
        byte fourth = 97;
        short fifth = -10000;

        Console.WriteLine("{0} -> ushort\n{1} -> sbyte\n{2} -> int\n{3} -> byte\n{4} -> short", first, second, third, fourth, fifth);
    }
}
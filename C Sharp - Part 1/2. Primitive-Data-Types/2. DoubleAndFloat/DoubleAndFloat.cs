using System;

class DoubleAndFloat
{
    static void Main()
    {
        double first = 34.567839023;
        float second = 12.345f;
        double third = 8923.1234567;
        float fourth = 3456.091f;
                
        Console.WriteLine("Double are:\n{0}\n{2}\n\nFloat are:\n{1}\n{3}",first, second, third, fourth);
    }
}
